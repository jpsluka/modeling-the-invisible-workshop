from __future__ import annotations

"""
Manual plotting for the workshop repository.

This script is a separate, manually-run version of plot_predictions.py.
It preserves older plots by writing figures into a distinct output directory
and uses unique filenames so the results do not conflict with the automated
plots generated by the original script.

Compared with the automated version, team prediction lines are clipped so that
only the prediction period is plotted; any earlier points that may appear in the
CSV files are excluded from the team traces.

Expected repository layout:

data-release/
  challenge-01/
    release-01/
      release_info.json
      public.csv
    release-02/
      release_info.json
      public.csv
      truth_previous_round.csv
    release-03/
      release_info.json
      public.csv
      truth_previous_round.csv

predictions/
  Team-01/
    challenge-01/
      round-01.csv
      round-02.csv
      round-03.csv
  Team-02/
    ...

Output layout:

scoring/plots-manual/
  challenge-01/
    release-02/
      Team-01_manual_prediction_only.png
      Team-02_manual_prediction_only.png
      all-teams_manual_prediction_only.png
    release-03/
      Team-01_manual_prediction_only.png
      Team-02_manual_prediction_only.png
      all-teams_manual_prediction_only.png
  challenge-02/
    ...
"""

from pathlib import Path
import csv
import json
import re

import matplotlib.pyplot as plt


ROOT = Path.cwd()
DATA_RELEASE_ROOT = ROOT / "data-release"
PREDICTIONS_ROOT = ROOT / "predictions"
PLOTS_ROOT = ROOT / "scoring" / "plots-manual"

TEAM_RE = re.compile(r"^Team-\d{2}$")
CHALLENGE_RE = re.compile(r"^challenge-\d{2}$")
RELEASE_RE = re.compile(r"^release-\d{2}$")
ROUND_RE = re.compile(r"^round-\d{2}\.csv$")

PLOT_SUFFIX = "_manual_prediction_only"


def read_csv(path: Path) -> list[dict[str, str]]:
    with path.open(newline="", encoding="utf-8") as f:
        return list(csv.DictReader(f))


def load_json(path: Path) -> dict:
    return json.loads(path.read_text(encoding="utf-8"))


def release_number(release_name: str) -> int:
    if not RELEASE_RE.match(release_name):
        raise ValueError(f"Invalid release folder name: {release_name}")
    return int(release_name.split("-")[1])


def latest_public_release_dir(challenge_name: str) -> Path:
    """
    Return the newest release folder for a challenge that contains public.csv.
    This is used only to find the public release trajectory for plots.
    """
    challenge_release_dir = DATA_RELEASE_ROOT / challenge_name
    if not challenge_release_dir.exists():
        raise FileNotFoundError(f"Missing challenge release folder: {challenge_release_dir}")

    release_dirs = sorted(
        [p for p in challenge_release_dir.iterdir() if p.is_dir() and RELEASE_RE.match(p.name)],
        key=lambda p: release_number(p.name),
    )

    for release_dir in reversed(release_dirs):
        if (release_dir / "public.csv").exists():
            return release_dir

    raise FileNotFoundError(f"No release with public.csv found for {challenge_name}")


def scoring_release_dirs(challenge_name: str) -> list[Path]:
    """
    Return all release folders for a challenge that close a previous round.
    """
    challenge_release_dir = DATA_RELEASE_ROOT / challenge_name
    if not challenge_release_dir.exists():
        return []

    release_dirs = sorted(
        [p for p in challenge_release_dir.iterdir() if p.is_dir() and RELEASE_RE.match(p.name)],
        key=lambda p: release_number(p.name),
    )

    scoring_dirs: list[Path] = []
    for release_dir in release_dirs:
        info_path = release_dir / "release_info.json"
        if not info_path.exists():
            continue
        try:
            info = load_json(info_path)
        except json.JSONDecodeError:
            continue
        if "scores_round_id" in info:
            scoring_dirs.append(release_dir)

    return scoring_dirs


def load_public_data(release_dir: Path) -> tuple[list[int], list[float], dict]:
    """
    Load public.csv and release_info.json from a release directory.
    """
    public_path = release_dir / "public.csv"
    info_path = release_dir / "release_info.json"

    if not public_path.exists():
        raise FileNotFoundError(f"Missing public data file: {public_path}")
    if not info_path.exists():
        raise FileNotFoundError(f"Missing release metadata file: {info_path}")

    rows = read_csv(public_path)
    info = load_json(info_path)

    weeks = [int(r["week"]) for r in rows]
    hosp = [float(r["hospitalizations_per_100k"]) for r in rows]
    return weeks, hosp, info


def team_prediction_file(team_dir: Path, challenge_name: str, round_id: int) -> Path | None:
    pred_path = team_dir / challenge_name / f"round-{round_id:02d}.csv"
    return pred_path if pred_path.exists() else None


def prediction_start_week(release_info: dict) -> int | None:
    """
    Prefer the forecast start week when available. Fall back to the scoring
    round start week if that is the only field present.
    """
    #start_week = release_info.get("forecast_start_week")
    start_week = release_info.get("scores_round_start_week")
    if start_week is None:
        start_week = release_info.get("scores_round_start_week")
    return int(start_week) if start_week is not None else None


def filter_prediction_rows(rows: list[dict[str, str]], start_week: int | None) -> tuple[list[int], list[float]]:
    """
    Keep only rows that fall inside the prediction period.
    """
    filtered_rows = rows
    if start_week is not None:
        filtered_rows = [r for r in rows if int(r["week"]) >= start_week]

    weeks = [int(r["week"]) for r in filtered_rows]
    hosp = [float(r["hospitalizations_per_100k"]) for r in filtered_rows]
    return weeks, hosp


def plot_team_for_release(team_dir: Path, challenge_name: str, release_dir: Path) -> None:
    """
    Create a team-specific plot for a specific scoring release.
    """
    public_weeks, public_hosp, release_info = load_public_data(release_dir)

    if "scores_round_id" not in release_info:
        return

    round_id = int(release_info["scores_round_id"])
    pred_path = team_prediction_file(team_dir, challenge_name, round_id)
    if pred_path is None:
        return

    rows = read_csv(pred_path)
    start_week = prediction_start_week(release_info)
    weeks, hosp = filter_prediction_rows(rows, start_week)

    out_dir = PLOTS_ROOT / challenge_name / release_dir.name
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / f"{team_dir.name}{PLOT_SUFFIX}.png"

    fig, ax = plt.subplots(figsize=(7, 3))

    ax.plot(
        public_weeks,
        public_hosp,
        linewidth=1.2,
        marker="o",
        markersize=3,
        label=f"Released data ({release_dir.name})",
    )

    ax.plot(
        weeks,
        hosp,
        linewidth=1.0,
        markersize=2,
        marker="o",
        linestyle="--",
        label=f"{team_dir.name} {pred_path.stem}",
    )

    if start_week is not None:
        ax.axvline(
            int(start_week),
            linestyle=":",
            linewidth=1.0,
            alpha=0.7,
            label="Forecast start",
        )

    ax.set_title(f"{team_dir.name} - {challenge_name} - {release_dir.name}")
    ax.set_xlabel("Week")
    ax.set_ylabel("Hosp / 100k")
    ax.grid(True, alpha=0.3)
    ax.legend(fontsize=6)

    fig.tight_layout()
    fig.savefig(out_path, dpi=150, bbox_inches="tight")
    plt.close(fig)
    print(f"Wrote {out_path}")


def plot_all_teams_for_release(challenge_name: str, release_dir: Path) -> None:
    """
    Create an aggregate plot for a specific scoring release:
    released data + one line per team, no markers.
    Team traces are clipped to the prediction period only.
    """
    public_weeks, public_hosp, release_info = load_public_data(release_dir)

    if "scores_round_id" not in release_info:
        return

    round_id = int(release_info["scores_round_id"])
    start_week = prediction_start_week(release_info)

    out_dir = PLOTS_ROOT / challenge_name / release_dir.name
    out_dir.mkdir(parents=True, exist_ok=True)
    out_path = out_dir / f"all-teams{PLOT_SUFFIX}.png"

    fig, ax = plt.subplots(figsize=(10, 4))

    ax.plot(
        public_weeks,
        public_hosp,
        linewidth=1.4,
        label=f"Released data ({release_dir.name})",
    )

    for team_dir in sorted([p for p in PREDICTIONS_ROOT.iterdir() if p.is_dir() and TEAM_RE.match(p.name)]):
        pred_path = team_prediction_file(team_dir, challenge_name, round_id)
        if pred_path is None:
            continue

        rows = read_csv(pred_path)
        weeks, hosp = filter_prediction_rows(rows, start_week)

        ax.plot(
            weeks,
            hosp,
            linewidth=1.0,
            label=f"{team_dir.name} {pred_path.stem}",
        )

    if start_week is not None:
        ax.axvline(
            int(start_week),
            linestyle=":",
            linewidth=1.0,
            alpha=0.7,
            label="Forecast start",
        )

    ax.set_title(f"All teams - {challenge_name} - {release_dir.name}")
    ax.set_xlabel("Week")
    ax.set_ylabel("Hosp / 100k")
    ax.grid(True, alpha=0.3)
    ax.legend(fontsize=6)

    fig.tight_layout()
    fig.savefig(out_path, dpi=150, bbox_inches="tight")
    plt.close(fig)
    print(f"Wrote {out_path}")


def main() -> None:
    if not PREDICTIONS_ROOT.exists():
        raise FileNotFoundError(f"Missing predictions folder: {PREDICTIONS_ROOT}")
    if not DATA_RELEASE_ROOT.exists():
        raise FileNotFoundError(f"Missing data-release folder: {DATA_RELEASE_ROOT}")

    challenge_names = sorted(
        {
            challenge_dir.name
            for team_dir in PREDICTIONS_ROOT.iterdir()
            if team_dir.is_dir() and TEAM_RE.match(team_dir.name)
            for challenge_dir in team_dir.iterdir()
            if challenge_dir.is_dir() and CHALLENGE_RE.match(challenge_dir.name)
        }
    )

    if not challenge_names:
        print("No team challenge folders found. Nothing to plot.")
        return

    for challenge_name in challenge_names:
        releases = scoring_release_dirs(challenge_name)
        if not releases:
            print(f"No scoring releases found for {challenge_name}; skipping.")
            continue

        for release_dir in releases:
            for team_dir in sorted([p for p in PREDICTIONS_ROOT.iterdir() if p.is_dir() and TEAM_RE.match(p.name)]):
                plot_team_for_release(team_dir, challenge_name, release_dir)

            plot_all_teams_for_release(challenge_name, release_dir)

    print("Finished generating manual plots.")


if __name__ == "__main__":
    main()
